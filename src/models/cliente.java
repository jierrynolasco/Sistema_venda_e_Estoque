package models;

public class cliente {
    private String nome;

    private String cpf;

    private String email;

    private String telefone;

    public cliente () {

    }

    public cliente(String nome, String cpf, String email, String telefone) {
        this.nome = nome;
        this.cpf = cpf;
        this.email = email;
        this.telefone = telefone;
    }


}
